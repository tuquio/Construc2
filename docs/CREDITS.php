Construc2 is a HTML5 Template for Joomla 2.5 and carries several ideas
inspired by "Construct Community Template Framework for Joomla! 1.6".
It has since been heavily refactored after being forked on gihub.com on
June 20th, 2011. See README for changes and features.

Construc2 includes OOCSS by Nicole 'stubornella' Sullivan, being the most
lightweight, yet flexible and "done right" CSS library around (the other
"done right" framework is YAML, but a too heavy lifting for my taste.)

Special thanks to marineart.de and branz-consulting.de for allowing the use of
several layout and styling techniques "to challenge" their then needs in J!1.5
back in 2007/2008, and built into their websites: (in no particular order)
 - include static HTML "snippet files" Module
 - alternative Homepage layout override
 - dynamic menu item alias as a more powerful replacement for "pageclass_sfx"
 - dynamic category alias based CSS
 - parametrised Module Chrome based on attributes
 - content plugin events in Module output
 - extended and semantic "mainmenu" (later aka "Better mod_menu")
 - CSS packer proxy based on CSSTidy

